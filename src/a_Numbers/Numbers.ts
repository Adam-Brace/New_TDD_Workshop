// Create a function that sums two numbers
export function adds(a: number, b: number): number {
	return a + b;
}

//Create a function that subtracts two numbers
export function subtracts(a: number, b: number): number {
	return a - b;
}

//Create a function that multiplies two numbers
export function multiplies(a: number, b: number): number {
	return a * b;
}
